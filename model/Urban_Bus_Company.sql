BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "STOP" (
	"stop_id"	INTEGER NOT NULL,
	"stop_name"	TEXT NOT NULL,
	"gps_latitude"	REAL NOT NULL,
	"gps_longtitude"	REAL NOT NULL,
	PRIMARY KEY("stop_id" AUTOINCREMENT)
);

CREATE TABLE IF NOT EXISTS "HAS_STOP" (
	"stops_order" INTEGER NOT NULL,
	"next_stop"	INTEGER,
	"previous_stop"	INTEGER,
	"line_number"	INTEGER NOT NULL,
	"time_difference"	INTEGER,
	PRIMARY KEY("next_stop","previous_stop","line_number"),
	FOREIGN KEY("next_stop") REFERENCES "STOP"("stop_id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("previous_stop") REFERENCES "STOP"("stop_id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("line_number") REFERENCES "LINE"("line_number") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "SCHEDULED_ROUTE" (
	"scheduled_route_id" INTEGER NOT NULL ,
	"line_number"	INTEGER NOT NULL,
	"day"	TEXT NOT NULL,
	"hour"	TIME,
	PRIMARY KEY("scheduled_route_id" AUTOINCREMENT),
	FOREIGN KEY("line_number") REFERENCES "LINE"("line_number") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "ROUTE" (
	"route_id"	INTEGER NOT NULL ,
	"scheduled_route_id"	INTEGER NOT NULL,
	PRIMARY KEY("route_id" AUTOINCREMENT),
	FOREIGN KEY("scheduled_route_id") REFERENCES "SCHEDULED_ROUTE"("scheduled_route_id") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "HAS_VISITED" (
	"route_id"	INTEGER NOT NULL,
	"stop_id"	INTEGER NOT NULL,
	"timestamp"	DATETIME NOT NULL,
	PRIMARY KEY("stop_id","route_id","timestamp"),
	FOREIGN KEY("stop_id") REFERENCES "STOP"("stop_id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("route_id") REFERENCES "ROUTE"("route_id") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "DRIVER" (
	"ssn"	INTEGER NOT NULL,
	"first_name"	TEXT NOT NULL,
	"last_name"	TEXT NOT NULL,
	"birth_date"	DATE,
	"license_number"	TEXT NOT NULL UNIQUE,
	PRIMARY KEY("ssn")
);

CREATE TABLE IF NOT EXISTS "OPERATES" (
	"bus_id"	INTEGER NOT NULL,
	"ssn"	INTEGER NOT NULL,
	"route_id"	INTEGER NOT NULL,
	"starts_at_datetime"	DATETIME NOT NULL,
	PRIMARY KEY("ssn","bus_id","route_id"),
	FOREIGN KEY("ssn") REFERENCES "DRIVER"("ssn") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("bus_id") REFERENCES "BUS"("bus_id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("route_id") REFERENCES "ROUTE"("route_id") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "POSITION" (
	"position_id"	INTEGER NOT NULL ,
	"gps_latitude"	REAL NOT NULL,
	"gps_longtitude"	REAL NOT NULL,
	"accuracy"	REAL,
	PRIMARY KEY("position_id" AUTOINCREMENT)
);

CREATE TABLE IF NOT EXISTS "BUS" (
	"bus_id"	INTEGER NOT NULL ,
	"plate_number"	TEXT NOT NULL UNIQUE,
	"position_timestamp"	TEXT NOT NULL,
	"position_id"	INTEGER NOT NULL,
	PRIMARY KEY("bus_id" AUTOINCREMENT),
	FOREIGN KEY("position_id") REFERENCES "POSITION"("position_id") ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS "LINE" (
	"line_number"	INTEGER NOT NULL,
	"line_name"	TEXT NOT NULL,
	PRIMARY KEY("line_number")
);

